// Helper function to get all category IDs for a slug (including children)
export const getAllCategoryIdsForSlug = (slug) => {
  const normalizedSlug = slug?.toLowerCase()?.replace(/\s+/g, '-') || 'wingfoil';
  const slugCategories = FILTER_CATEGORIES[normalizedSlug] || FILTER_CATEGORIES[slug] || [];
  
  const allIds = [];
  slugCategories.forEach(category => {
    allIds.push(category.id);
    if (category.children) {
      category.children.forEach(child => allIds.push(child.id));
    }
  });
  
  // Deduplicate IDs
  const uniqueIds = [...new Set(allIds)];
  
  return uniqueIds;
};
